    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        // Verificar si el campo txtusuario está vacío
    String usuario = txtusuario.getText().trim();
    UsuariosDao usDao = new UsuariosDao();
    
    if (!usDao.usuarioExiste(usuario)) {
            //JOptionPane.showMessageDialog(null, "El usuario no existe en el sistema.", "Usuario no encontrado", JOptionPane.WARNING_MESSAGE);
            RecCorreo r = new RecCorreo();
            r.advertencia("Usuario no encontrado o no existe en el sistema");
            return; // Salir del método si el usuario no existe
        }
    
    if (usuario.isEmpty()) {
        //JOptionPane.showMessageDialog(null, "El campo de usuario no debe estar vacío.", "Campo Vacío", JOptionPane.WARNING_MESSAGE);
        RecCorreo r = new RecCorreo();
        r.advertencia("El campo de usuario no debe estar vacío.");
        return; // Salir del método si el campo está vacío
    }

    // Si el campo de usuario no está vacío, proceder con el envío
    Usuarios usuarios = new Usuarios();
    UsuariosDao ussDao = new UsuariosDao();
    String user;
    user = this.txtusuario.getText();
    Ctrl_User ctrl = new Ctrl_User(usuarios, ussDao);
    ctrl.Iniciar();
    if (ctrl.Enviar(user)) {
        this.txtusuario.setText("");
        this.txtenvio1.setText(ctrl.destinatario);
    }
    }//GEN-LAST:event_btnEnviarActionPerformed

