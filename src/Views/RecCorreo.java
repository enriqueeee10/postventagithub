    private void verificarCodigo() {

    String codigoAleatorio = NumeroAleatorio.generarCodigoAleatorio();
    String codigoIngresado = txtcodigo.getText();
    
    if (codigoIngresado.isEmpty()) {
    //JOptionPane.showMessageDialog(null, "El campo de código no debe estar vacío.", "Campo Vacío", JOptionPane.WARNING_MESSAGE);
    RecCorreo r = new RecCorreo();
    r.advertencia("El campo de código no debe estar vacío.");
    return;
    }
    if (codigoIngresado.equals(codigoAleatorio)) {
    ingresar();
    } else {
    //JOptionPane.showMessageDialog(null, "El código ingresado no es válido. Por favor, inténtelo de nuevo.", "Error", JOptionPane.ERROR_MESSAGE);
    RecCorreo r = new RecCorreo();
    r.error("El código ingresado no es válido.");
    }
    }
    
